function createSeededRandom(seed: string) {
  let h = 0;
  for (let i = 0; i < seed.length; i++) {
    h = (Math.imul(31, h) + seed.charCodeAt(i)) | 0;
  }
  return function () {
    const x = Math.sin(h++) * 10000;
    return x - Math.floor(x);
  };
}

const TIER_SCORE_MIN: Record<AuraTier, number> = {
  [AuraTier.COMMON]: 0,
  [AuraTier.UNCOMMON]: 40,
  [AuraTier.RARE]: 60,
  [AuraTier.EPIC]: 75,
  [AuraTier.LEGENDARY]: 85,
  [AuraTier.MYTHIC]: 92,
  [AuraTier.SECRET]: 98,
};

export function generateAuraCard(rawUsername: string, forceReRollSeed?: string): AuraCardData {
  const username = rawUsername.trim().replace(/^@/, '');

  const seedString = `${username.toLowerCase()}_${forceReRollSeed || 'primary_v1'}`;
  const rand = createSeededRandom(seedString);

  const metricsPool = [...METRICS_CATALOG];
  const selectedMetrics: AuraMetric[] = [];

  for (let i = 0; i < 8; i++) {
    const idx = Math.floor(rand() * metricsPool.length);
    const item = metricsPool.splice(idx, 1)[0];
    const score = Math.floor(20 + rand() * 73);
    selectedMetrics.push({
      id: item.id,
      emoji: item.emoji,
      label: item.label,
      description: item.description,
      score,
    });
  }
  selectedMetrics.sort((a, b) => b.score - a.score);

  const highCount = selectedMetrics.filter((m) => m.score >= 78).length;
  const randomOffset = rand() < 0.15 ? 1 : rand() < 0.2 ? -1 : 0;
  const adjustedHigh = Math.max(0, Math.min(8, highCount + randomOffset));

  let tier: AuraTier;
  if (adjustedHigh >= 7) {
    tier = AuraTier.LEGENDARY;
    const sub = rand() * 100;
    if (sub < 5) tier = AuraTier.SECRET;
    else if (sub < 25) tier = AuraTier.MYTHIC;
  } else if (adjustedHigh >= 5) {
    tier = AuraTier.EPIC;
  } else if (adjustedHigh >= 4) {
    tier = AuraTier.RARE;
  } else if (adjustedHigh >= 2) {
    tier = AuraTier.UNCOMMON;
  } else {
    tier = AuraTier.COMMON;
  }

  const matchingTitles = TITLES_CATALOG.filter((t) => t.tier === tier);
  const titleList = matchingTitles.length > 0 ? matchingTitles : TITLES_CATALOG.filter((t) => t.tier === AuraTier.COMMON);
  const title = titleList[Math.floor(rand() * titleList.length)];

  const minScore = TIER_SCORE_MIN[tier];
  for (const m of selectedMetrics) {
    if (m.score < minScore) {
      m.score = minScore + Math.floor(rand() * 10);
      if (m.score > 100) m.score = 100;
    }
  }
  selectedMetrics.sort((a, b) => b.score - a.score);

  const badgesPool = [...BADGES_CATALOG];
  const selectedBadges: AuraBadge[] = [];
  const badgeCount = Math.min(4, Math.floor(rand() * 2) + 3);
  for (let i = 0; i < badgeCount; i++) {
    const idx = Math.floor(rand() * badgesPool.length);
    selectedBadges.push(badgesPool.splice(idx, 1)[0]);
  }

  const insight = INSIGHTS_CATALOG[Math.floor(rand() * INSIGHTS_CATALOG.length)];

  return {
    username: `@${username}`,
    title,
    tier,
    metrics: selectedMetrics,
    badges: selectedBadges,
    insight,
    generatedAt: new Date().toLocaleDateString('en-US', { month: 'short', year: 'numeric' }),
  };
}
